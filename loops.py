# Break and Continue.

# 1) Break.
print("The Break Statement: ")
for x in range(10):
    if(x==6):
        break
    print(x)
    

# 2) Continue.
print("The Continue Statement: ")
for x in range(11):
    if(x==6):
        continue
    print(x)
    










